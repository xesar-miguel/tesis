package pe.gob.susalud.renipress.dominio.entidades;

public class PerfilWS {

	private String codopc;
	private String codtop;
	private String nombopc;
	private String descopc;
	private String ordenopc;
	private String estaopc;
	private String accesos;
	public String getCodopc() {
		return codopc;
	}
	public void setCodopc(String codopc) {
		this.codopc = codopc;
	}
	public String getCodtop() {
		return codtop;
	}
	public void setCodtop(String codtop) {
		this.codtop = codtop;
	}
	public String getNombopc() {
		return nombopc;
	}
	public void setNombopc(String nombopc) {
		this.nombopc = nombopc;
	}
	public String getDescopc() {
		return descopc;
	}
	public void setDescopc(String descopc) {
		this.descopc = descopc;
	}
	public String getOrdenopc() {
		return ordenopc;
	}
	public void setOrdenopc(String ordenopc) {
		this.ordenopc = ordenopc;
	}
	public String getEstaopc() {
		return estaopc;
	}
	public void setEstaopc(String estaopc) {
		this.estaopc = estaopc;
	}
	public String getAccesos() {
		return accesos;
	}
	public void setAccesos(String accesos) {
		this.accesos = accesos;
	}
	
	
}
